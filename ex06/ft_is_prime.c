int	ft_is_prime(int nb)
{
	int	a;
	int	b;

	a = 2;
	b = nb - 1;
	if (nb < 0)
		return (0);
	if (nb == 0)
		return (0);
	if (nb == 1)
		return (0);
	while (a <= b)
	{
		if ((nb % a) == 0)
			return (0);
		a++;
	}
	return (1);
}
