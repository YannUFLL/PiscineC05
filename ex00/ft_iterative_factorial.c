int	ft_iterative_factorial(int nb)
{
	int	a;
	int	b;

	a = 1;
	b = 1;
	if ( nb < 0)
		return (0);
	while (a <= nb)
	{
		b = b * a;
		a++;
	}
	return (b);
}
